#include <stdio.h>
#include <stdlib.h>

int main()
{
    int dim;
    printf("Dim: ");
    scanf("%d", &dim);

    for(int t = 0; t < dim; t = t + 1) {
        for(int u = 0; u < dim; u = u + 1) {
            printf("#");
        }
        printf("\n");
    }
    return 0;
}
